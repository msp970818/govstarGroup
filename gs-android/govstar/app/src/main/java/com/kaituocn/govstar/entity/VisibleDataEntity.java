package com.kaituocn.govstar.entity;

public class VisibleDataEntity {

    private String allTaskNum;
    private String completeTaskNum;
    private String delayTaskNum;

    public String getAllTaskNum() {
        return allTaskNum;
    }

    public void setAllTaskNum(String allTaskNum) {
        this.allTaskNum = allTaskNum;
    }

    public String getCompleteTaskNum() {
        return completeTaskNum;
    }

    public void setCompleteTaskNum(String completeTaskNum) {
        this.completeTaskNum = completeTaskNum;
    }

    public String getDelayTaskNum() {
        return delayTaskNum;
    }

    public void setDelayTaskNum(String delayTaskNum) {
        this.delayTaskNum = delayTaskNum;
    }
}
